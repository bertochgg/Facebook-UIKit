import UIKit

extension UIColor {
    
    @nonobjc class var facebookBlue: UIColor {
        // #4267b2 = rgba(66,103,178,255)
        return UIColor(red: 66.0 / 255.0, green: 103.0 / 255.0, blue: 178.0 / 255.0, alpha: 1.0)
    }
}
